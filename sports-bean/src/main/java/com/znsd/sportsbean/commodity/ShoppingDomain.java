package com.znsd.sportsbean.commodity;

import java.util.List;

/**
 * 购物车表（shoppingcart_db）
 */
public class ShoppingDomain {
    private String id;//购物车编号
    private String userId;//用户编号
    private String commodityid;//商品编号
    private Integer number;//商品数量
    private List<CommodityDomain> list;

    public List<CommodityDomain> getList() {
        return list;
    }

    public void setList(List<CommodityDomain> list) {
        this.list = list;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getCommodityid() {
        return commodityid;
    }

    public void setCommodityid(String commodityid) {
        this.commodityid = commodityid;
    }

    public Integer getNumber() {
        return number;
    }

    public void setNumber(Integer number) {
        this.number = number;
    }


    @Override
    public String toString() {
        return "ShoppingDomain{" +
                "id='" + id + '\'' +
                ", userId='" + userId + '\'' +
                ", commodityid='" + commodityid + '\'' +
                ", number=" + number +
                ", list=" + list +
                '}';
    }
}
