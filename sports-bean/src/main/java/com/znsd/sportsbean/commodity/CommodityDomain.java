package com.znsd.sportsbean.commodity;

import java.io.Serializable;

/**
 * 商品表（commodity_db）,与商品类型表关联（commodittype_db）
 */
public class CommodityDomain implements Serializable {
    private String id;//商品编号
    private String name;//商品名称
    private String bewrite;//描述
    private Double price;//单价
    private Integer inventory;//积分
    private Integer sales;//总销量
    private String image;//图片地址
    private Integer sum;//库存
    private String typeId;//商品类型编号

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBewrite() {
        return bewrite;
    }

    public void setBewrite(String bewrite) {
        this.bewrite = bewrite;
    }

    public Double getPrice() {
        return price;
    }

    public void setPrice(Double price) {
        this.price = price;
    }

    public Integer getInventory() {
        return inventory;
    }

    public void setInventory(Integer inventory) {
        this.inventory = inventory;
    }

    public Integer getSales() {
        return sales;
    }

    public void setSales(Integer sales) {
        this.sales = sales;
    }

    public String getImage() {
        return image;
    }

    public void setImage(String image) {
        this.image = image;
    }

    public Integer getSum() {
        return sum;
    }

    public void setSum(Integer sum) {
        this.sum = sum;
    }

    public String getTypeId() {
        return typeId;
    }

    public void setTypeId(String typeId) {
        this.typeId = typeId;
    }

    @Override
    public String toString() {
        return "commodityDomain{" +
                "id='" + id + '\'' +
                ", name='" + name + '\'' +
                ", bewrite='" + bewrite + '\'' +
                ", price=" + price +
                ", inventory=" + inventory +
                ", sales=" + sales +
                ", image='" + image + '\'' +
                ", sum=" + sum +
                ", typeId='" + typeId + '\'' +
                '}';
    }
}
