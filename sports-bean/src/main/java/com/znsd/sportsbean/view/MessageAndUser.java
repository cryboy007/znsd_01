package com.znsd.sportsbean.view;

import java.sql.Timestamp;

public class MessageAndUser {
    private Integer id;
    private String name;
    private String content;
    private String userId;
    private String uname;
    private String username;
    private Integer status;
    private String typeId;
    private Timestamp times;

    public MessageAndUser() {
    }

    public MessageAndUser(Integer id, String name, String content, String userId, String uname, String username, Integer status, String typeId, Timestamp times) {
        this.id = id;
        this.name = name;
        this.content = content;
        this.userId = userId;
        this.uname = uname;
        this.username = username;
        this.status = status;
        this.typeId = typeId;
        this.times = times;
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getContent() {
        return content;
    }

    public void setContent(String content) {
        this.content = content;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getUname() {
        return uname;
    }

    public void setUname(String uname) {
        this.uname = uname;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public Integer getStatus() {
        return status;
    }

    public void setStatus(Integer status) {
        this.status = status;
    }

    public String getTypeId() {
        return typeId;
    }

    public void setTypeId(String typeId) {
        this.typeId = typeId;
    }

    public Timestamp getTimes() {
        return times;
    }

    public void setTimes(Timestamp times) {
        this.times = times;
    }

    @Override
    public String toString() {
        return "MessageAndUser{" +
                "id=" + id +
                ", name='" + name + '\'' +
                ", content='" + content + '\'' +
                ", userId='" + userId + '\'' +
                ", uname='" + uname + '\'' +
                ", username='" + username + '\'' +
                ", status=" + status +
                ", typeId='" + typeId + '\'' +
                ", times=" + times +
                '}';
    }
}
