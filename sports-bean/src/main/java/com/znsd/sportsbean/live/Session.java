package com.znsd.sportsbean.live;

public class Session {
    private String Id;
    private String Name;
    private String Type;
    private String Application;
    private String HLS;
    private String HTTPFLV;
    private String WSFLV;
    private String RTMP;
    private String Time;
    private Integer NumOutputs;
    private Integer InBytes;
    private Integer OutBytes;
    private Integer InBitrate;
    private Integer OutBitrate;
    private Integer AudioBitrate;
    private Integer VideoBitrate;
    private String StartTime;

    public Session() {
    }

    public Session(String id, String name, String type, String application, String HLS, String HTTPFLV, String WSFLV, String RTMP, String time, Integer numOutputs, Integer inBytes, Integer outBytes, Integer inBitrate, Integer outBitrate, Integer audioBitrate, Integer videoBitrate, String startTime) {
        Id = id;
        Name = name;
        Type = type;
        Application = application;
        this.HLS = HLS;
        this.HTTPFLV = HTTPFLV;
        this.WSFLV = WSFLV;
        this.RTMP = RTMP;
        Time = time;
        NumOutputs = numOutputs;
        InBytes = inBytes;
        OutBytes = outBytes;
        InBitrate = inBitrate;
        OutBitrate = outBitrate;
        AudioBitrate = audioBitrate;
        VideoBitrate = videoBitrate;
        StartTime = startTime;
    }

    public String getId() {
        return Id;
    }

    public void setId(String id) {
        Id = id;
    }

    public String getName() {
        return Name;
    }

    public void setName(String name) {
        Name = name;
    }

    public String getType() {
        return Type;
    }

    public void setType(String type) {
        Type = type;
    }

    public String getApplication() {
        return Application;
    }

    public void setApplication(String application) {
        Application = application;
    }

    public String getHLS() {
        return HLS;
    }

    public void setHLS(String HLS) {
        this.HLS = HLS;
    }

    public String getHTTPFLV() {
        return HTTPFLV;
    }

    public void setHTTPFLV(String HTTPFLV) {
        this.HTTPFLV = HTTPFLV;
    }

    public String getWSFLV() {
        return WSFLV;
    }

    public void setWSFLV(String WSFLV) {
        this.WSFLV = WSFLV;
    }

    public String getRTMP() {
        return RTMP;
    }

    public void setRTMP(String RTMP) {
        this.RTMP = RTMP;
    }

    public String getTime() {
        return Time;
    }

    public void setTime(String time) {
        Time = time;
    }

    public Integer getNumOutputs() {
        return NumOutputs;
    }

    public void setNumOutputs(Integer numOutputs) {
        NumOutputs = numOutputs;
    }

    public Integer getInBytes() {
        return InBytes;
    }

    public void setInBytes(Integer inBytes) {
        InBytes = inBytes;
    }

    public Integer getOutBytes() {
        return OutBytes;
    }

    public void setOutBytes(Integer outBytes) {
        OutBytes = outBytes;
    }

    public Integer getInBitrate() {
        return InBitrate;
    }

    public void setInBitrate(Integer inBitrate) {
        InBitrate = inBitrate;
    }

    public Integer getOutBitrate() {
        return OutBitrate;
    }

    public void setOutBitrate(Integer outBitrate) {
        OutBitrate = outBitrate;
    }

    public Integer getAudioBitrate() {
        return AudioBitrate;
    }

    public void setAudioBitrate(Integer audioBitrate) {
        AudioBitrate = audioBitrate;
    }

    public Integer getVideoBitrate() {
        return VideoBitrate;
    }

    public void setVideoBitrate(Integer videoBitrate) {
        VideoBitrate = videoBitrate;
    }

    public String getStartTime() {
        return StartTime;
    }

    public void setStartTime(String startTime) {
        StartTime = startTime;
    }

    @Override
    public String toString() {
        return "Session{" +
                "Id='" + Id + '\'' +
                ", Name='" + Name + '\'' +
                ", Type='" + Type + '\'' +
                ", Application='" + Application + '\'' +
                ", HLS='" + HLS + '\'' +
                ", HTTPFLV='" + HTTPFLV + '\'' +
                ", WSFLV='" + WSFLV + '\'' +
                ", RTMP='" + RTMP + '\'' +
                ", Time='" + Time + '\'' +
                ", NumOutputs=" + NumOutputs +
                ", InBytes=" + InBytes +
                ", OutBytes=" + OutBytes +
                ", InBitrate=" + InBitrate +
                ", OutBitrate=" + OutBitrate +
                ", AudioBitrate=" + AudioBitrate +
                ", VideoBitrate=" + VideoBitrate +
                ", StartTime='" + StartTime + '\'' +
                '}';
    }
}
