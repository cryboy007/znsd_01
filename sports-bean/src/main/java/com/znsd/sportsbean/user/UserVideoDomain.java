package com.znsd.sportsbean.user;

/**
 * 用户与视频合集的点赞收藏评分表（user_video_db）
 * 和用户表（user_db）,视频合集表（video_collection）关联
 */
public class UserVideoDomain {
    private String id;//编号
    private String userId;//用户编号
    private String videoId;//视频合集编号
    private Double score;//评分
    private Integer like;//点赞
    private Integer collect;//收藏

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public String getVideoId() {

        return videoId;
    }

    public void setVideoId(String videoId) {
        this.videoId = videoId;
    }

    public Double getScore() {
        return score;
    }

    public void setScore(Double score) {
        this.score = score;
    }

    public Integer getLike() {
        return like;
    }

    public void setLike(Integer like) {
        this.like = like;
    }

    public Integer getCollect() {
        return collect;
    }

    public void setCollect(Integer collect) {
        this.collect = collect;
    }

    @Override
    public String toString() {
        return "UserVideoDomain{" +
                "id='" + id + '\'' +
                ", userId='" + userId + '\'' +
                ", videoId='" + videoId + '\'' +
                ", score=" + score +
                ", like=" + like +
                ", collect=" + collect +
                '}';
    }
}
