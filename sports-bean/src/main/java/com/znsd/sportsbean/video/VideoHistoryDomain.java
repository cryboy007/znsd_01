package com.znsd.sportsbean.video;

import java.util.Date;

/**
 * 视频浏览记录表（videohistory_db）
 */
public class VideoHistoryDomain {
    private String id;//视频浏览编号
    private String videoId;//视频集合编号
    private String userId;//用户编号
    private Date times;//浏览时间

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getVideoId() {
        return videoId;
    }

    public void setVideoId(String videoId) {
        this.videoId = videoId;
    }

    public String getUserId() {
        return userId;
    }

    public void setUserId(String userId) {
        this.userId = userId;
    }

    public Date getDate() {
        return times;
    }

    public void setDate(Date times) {
        this.times = times;
    }

    public Date getTimes() {
        return times;
    }

    public void setTimes(Date times) {
        this.times = times;
    }

    @Override
    public String toString() {
        return "VideoHistoryDomain{" +
                "id='" + id + '\'' +
                ", videoId='" + videoId + '\'' +
                ", userId='" + userId + '\'' +
                ", times=" + times +
                '}';
    }
}
